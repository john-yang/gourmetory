				<p>Gone are the times when social media was just another networking opportunity brands could use to create a buzz. Social media has come a long way from being a silo where interns could learn new skills to a powerful platform that can boost a business’s bottom line.
</p><p>Today <a href="http://www.searchenginepeople.com/solutions/social-media-marketing">social media marketing</a> works in tandem with other business strategies to generate publicity and create awareness. Social media is a great place to start your marketing campaign from scratch.
</p><p>Follow these seven steps to create a killer <a href="http://www.searchenginepeople.com/solutions/social-media-marketing">SMM</a> strategy that resonates with your target audience:<br>
</p><h2 data-fontsize="24" data-lineheight="34">1. Establish Goals</h2>
<p>Any marketing strategy starts with goal setting.
</p><p>Analyzing the goals you want to achieve helps you come up with an attainable social media strategy. The goals keep you on the right track and allow you to gauge your ROI.
</p><p>Align your marketing goals with your business objectives. To make sure they are, look into the following four areas:
</p><ol>
<li>The networks on which you are currently active</li>
<li>How many of your networks are driving value</li>
<li>Check if all your networks are optimized to communicate your company culture </li>
<li>Identify your competitors and analyze their promotional activities </li>
</ol>
<p>Keep your goals specific, scalable, realistic, and most importantly time-bound.<br>
</p><h2 data-fontsize="24" data-lineheight="34">2. Audit Your Existing Social Medial Connect</h2>
<p>Before implementing your SMM strategy, evaluate your existing online reach.
</p><p>Create a spreadsheet where you can document the following details:
</p><ul>
<li>list the social networks you're actively using for brand promotions</li>
<li>Identify social networking platforms where you can showcase your brand</li>
<li>do all your profiles reflect your brand’s logo and vision? Make sure all the contact details are correct.</li>
<li>what is the target audience and what are their pain points </li>
<li>identify the preferred networking channels of your target audience</li>
</ul>
<p>Any marketing campaign aims to understand the needs and preferences of the end-user. The above pointers will help you learn more about your target audience and enable you to focus on effective channels.
